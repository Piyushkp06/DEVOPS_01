#!/usr/bin/env node
import { spawn, execSync } from "child_process";
import fs from "fs";
import os from "os";
import path from "path";

const CONFIG_DIR = path.join(os.homedir(), ".devops-cli");
const CONFIG_FILE = path.join(CONFIG_DIR, "config.json");

function readConfig() {
  try {
    return JSON.parse(fs.readFileSync(CONFIG_FILE, "utf8"));
  } catch (e) {
    return {};
  }
}

function writeConfig(cfg) {
  try {
    if (!fs.existsSync(CONFIG_DIR)) fs.mkdirSync(CONFIG_DIR, { recursive: true });
    fs.writeFileSync(CONFIG_FILE, JSON.stringify(cfg, null, 2));
  } catch (e) {
    console.error("Failed to write config:", e.message);
  }
}

async function ensureProxy() {
  const cfg = readConfig();
  if (cfg.proxyPid) {
    try {
      process.kill(cfg.proxyPid, 0);
      return cfg.proxyPid;
    } catch (e) {
      // dead -> fallthrough
    }
  }
  let podName = null;
  try {
    podName = execSync('kubectl get pods -n devops-platform -l app=node-backend -o jsonpath="{.items[0].metadata.name}"', { encoding: 'utf8' }).trim();
  } catch (e) {
    throw new Error('Failed to find node-backend pod: ' + e.message);
  }

  const p = spawn('kubectl', ['port-forward', `pod/${podName}`, '5001:5000', '-n', 'devops-platform'], { stdio: 'ignore', detached: true });
  p.unref();
  cfg.proxyPid = p.pid;
  writeConfig(cfg);
  await new Promise(r => setTimeout(r, 800));
  return p.pid;
}

function printHelp() {
  console.log(`Usage: devops-cli <command>\n\nCommands:\n  proxy-start        Start kubectl port-forward to node-backend (localhost:5001 -> pod:5000)\n  proxy-stop         Stop the port-forward started by proxy-start\n  login [email] [password]   Login and save token (defaults: system@devops-platform.local / autogenerated)\n  incidents [page] [limit]   List incidents\n  actions            List actions\n  execute <actionId> Execute an action by id\n  help               Show this help\n`);
}

async function httpPost(path, body = {}, token) {
  const url = `http://localhost:5001${path}`;
  const opts = {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(body)
  };
  if (token) opts.headers['Authorization'] = `Bearer ${token}`;
  const res = await fetch(url, opts);
  return res.json();
}

async function httpGet(path, token) {
  const url = `http://localhost:5001${path}`;
  const headers = token ? { Authorization: `Bearer ${token}` } : {};
  const res = await fetch(url, { headers });
  return res.json();
}

async function main(argv) {
  const cmd = argv[2];
  if (!cmd || cmd === 'help') return printHelp();

  if (cmd === 'proxy-start') {
    try {
      const pid = await ensureProxy();
      console.log('Started port-forward, pid:', pid);
    } catch (e) {
      console.error(e.message);
      process.exit(1);
    }
    return;
  }

  if (cmd === 'proxy-stop') {
    const cfg = readConfig();
    if (cfg.proxyPid) {
      try {
        process.kill(cfg.proxyPid);
        console.log('Stopped proxy pid', cfg.proxyPid);
        delete cfg.proxyPid;
        writeConfig(cfg);
      } catch (e) {
        console.error('Failed to stop proxy:', e.message);
      }
    } else {
      console.log('No proxy found in config.');
    }
    return;
  }

  if (cmd === 'login') {
    const email = argv[3] || 'system@devops-platform.local';
    const password = argv[4] || 'autogenerated';
    try {
      await ensureProxy();
      const data = await httpPost('/api/auth/login', { email, password });
      const token = data?.data?.accessToken || data?.data?.token || data?.accessToken || data?.token;
      if (!token) {
        console.error('Login succeeded but no token in response:', JSON.stringify(data));
        process.exit(1);
      }
      const cfg = readConfig(); cfg.token = token; writeConfig(cfg);
      console.log('Saved access token to config');
    } catch (e) {
      console.error('Login failed:', e.message || e);
      process.exit(1);
    }
    return;
  }

  const cfg = readConfig();
  if (cmd === 'incidents') {
    if (!cfg.token) return console.error('No token found. Run `devops-cli login` first.');
    const page = argv[3] || '1';
    const limit = argv[4] || '10';
    try {
      const data = await httpGet(`/api/incidents?page=${page}&limit=${limit}`, cfg.token);
      const incidents = data?.data?.incidents || data?.data || data;
      console.log('Incidents:');
      if (Array.isArray(incidents)) incidents.forEach(i => console.log('-', i.id, i.title, i.status));
      else if (incidents.incidents) incidents.incidents.forEach(i => console.log('-', i.id, i.title, i.status));
      else console.log(JSON.stringify(incidents, null, 2));
    } catch (e) {
      console.error('Failed to fetch incidents:', e.message || e);
      process.exit(1);
    }
    return;
  }

  if (cmd === 'actions') {
    if (!cfg.token) return console.error('No token found. Run `devops-cli login` first.');
    try {
      const data = await httpGet('/api/actions', cfg.token);
      const actions = data?.data || data;
      console.log('Actions:');
      if (Array.isArray(actions)) actions.forEach(a => console.log('-', a.id, a.type || a.name || a.command));
      else console.log(JSON.stringify(actions, null, 2));
    } catch (e) {
      console.error('Failed to fetch actions:', e.message || e);
      process.exit(1);
    }
    return;
  }

  if (cmd === 'execute') {
    const actionId = argv[3];
    if (!actionId) return console.error('actionId is required');
    if (!cfg.token) return console.error('No token found. Run `devops-cli login` first.');
    try {
      const data = await httpPost(`/api/actions/${actionId}/execute`, {}, cfg.token);
      console.log('Execute response:', JSON.stringify(data, null, 2));
    } catch (e) {
      console.error('Failed to execute action:', e.message || e);
      process.exit(1);
    }
    return;
  }

  printHelp();
}

main(process.argv).catch(err => { console.error(err); process.exit(1); });
